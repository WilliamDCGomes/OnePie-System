    private void consultar2(){
        String sqlnome = "select * from mecanicos where nome_mecanico=?";
        try {
            pst = conexao.prepareStatement(sqlnome);
            pst.setString(1,nomePeca.getText());
            rs = pst.executeQuery();
            if (rs.next()) {
                EditarMecanico usuario = new EditarMecanico();
                usuario.setVisible(true);
                usuario.cadastroNome.setText(rs.getString(1));
                String situacao=rs.getString(2);
                if(situacao.equals("ATIVO")){
                    usuario.statusAtivo.setSelected(true);
                }
                else if(situacao.equals("INATIVO")){
                    usuario.statusInativo.setSelected(true);
                }
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(null,"MECÂNICO NÃO ENCONTRADO NO BANCO DE DADOS!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }
