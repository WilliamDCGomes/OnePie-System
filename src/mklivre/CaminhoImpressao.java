    public void pupolaJComboBox(){
        String sql ="select * from tb_pecas_ml2 where QUANTIDADE < QUANTIDADE_MINIMA";
        try {
            pst = conexao.prepareStatement(sql);
            rs=pst.executeQuery();
            while(rs.next()){
                campoMecanicos.addItem(rs.getString("MARCA"));
            }
            combobox();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }
    public void combobox(){
        int quant=campoMecanicos.getItemCount();
        int valida=0;  
        String aux[]=new String[quant];
        for(int x=0;x<quant;x++){
            aux[x]=null;
        }
        int comp=1;
        while(quant>comp){
            for(int x=0;x<quant;x++){
                if(campoMecanicos.getItemAt(comp).equals(aux[x])){
                    valida=1;
                    break;
                }
            }
            if(valida==0){
                for(int x=0;x<quant;x++){
                    if(aux[x]==null){
                       aux[x]=campoMecanicos.getItemAt(comp);
                        break;
                    }
                }
            }
            valida=0;
            comp++;
        }
        mudar=1;
        campoMecanicos.removeAllItems();
        campoMecanicos.addItem("TODOS");
        for (int x=0;x<quant;x++){
            if(aux[x]!=null){
                campoMecanicos.addItem(aux[x]);
            }
        }
        mudar=0;
    }
    private void pesquisar(){
        String sql = "select CÓDIGO,NOME,MARCA,QUANTIDADE,QUANTIDADE_MINIMA from tb_pecas_ml2 where QUANTIDADE < QUANTIDADE_MINIMA";
        try {
            pst = conexao.prepareStatement(sql);
            rs=pst.executeQuery();
            tabelaDePecas.setModel(DbUtils.resultSetToTableModel(rs));
            mudar=1;
            campoMecanicos.removeAllItems();
            campoMecanicos.addItem("TODOS");
            pupolaJComboBox();
            mudar=0;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }
    private void pesquisarMarca(){
        String sql = "select CÓDIGO,NOME,LOCALIZAÇÃO,MARCA,QUANTIDADE,QUANTIDADE_RECOMENDADA from tb_pecas_ml2 where MARCA =?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1,campoMecanicos.getSelectedItem().toString());
            rs=pst.executeQuery();
            tabelaDePecas.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }
