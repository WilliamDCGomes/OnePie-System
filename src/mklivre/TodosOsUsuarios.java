    public void setar(){
        int setar=tabelaDeUsuarios.getSelectedRow();
        EditarUsuario execu = new EditarUsuario();
        execu.setVisible(true);
        execu.cadastroNome.setText(tabelaDeUsuarios.getModel().getValueAt(setar,0).toString());
        nivel.setText(tabelaDeUsuarios.getModel().getValueAt(setar,1).toString());
        if(Integer.parseInt(nivel.getText())==1){
            execu.nivelUm.setSelected(true);
        }
        else if(Integer.parseInt(nivel.getText())==2){
            execu.nivelDois.setSelected(true);
        }
        else if(Integer.parseInt(nivel.getText())==3){
            execu.nivelTres.setSelected(true);
        }
        else if(Integer.parseInt(nivel.getText())==4){
            execu.nivelQuatro.setSelected(true);
        }
        else if(Integer.parseInt(nivel.getText())==5){
            execu.nivelCinco.setSelected(true);
        }
        acesso.setText(tabelaDeUsuarios.getModel().getValueAt(setar,2).toString());
        if(acesso.getText().equals("AT")){
            execu.localAtendimento.setSelected(true);
        }
        else if(acesso.getText().equals("ES")){
            execu.localEs.setSelected(true);
        }
        else if(acesso.getText().equals("ML")){
            execu.localML.setSelected(true);
        }
        else if(acesso.getText().equals("OF")){
            execu.localOficina.setSelected(true);
        }
        sexo.setText(tabelaDeUsuarios.getModel().getValueAt(setar,3).toString());
        if(sexo.getText().equals("MASCULINO")){
            execu.sexoMascu.setSelected(true);
        }
        else if(sexo.getText().equals("FEMININO")){
            execu.sexoFemi.setSelected(true);
        }
        execu.mostrarNivel.setText(mostrarNivel.getText());
        execu.acessoUser.setText(acessoUser.getText());
    }
    private void pesquisar(){
        String sql = "select NOME,NIVEL,ACESSO_PERMITIDO,SEXO from tbusuarios2";
        try {
            pst = conexao.prepareStatement(sql);
            rs=pst.executeQuery();
            tabelaDeUsuarios.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }
