    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        if(x==0){
            x++;
            campoMecanicos.setSelectedItem("ONLINE");
        }
        if(campoMecanicos.getSelectedItem().toString().equals("TODOS")){
            pesquisar();
        }
        else if(campoMecanicos.getSelectedItem().toString().equals("ONLINE")){
            pesquisarOn();
        }
        else if(campoMecanicos.getSelectedItem().toString().equals("OFFLINE")){
            pesquisarOff();
        }
    }//GEN-LAST:event_formWindowActivated

