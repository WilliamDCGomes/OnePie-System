    private void procurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_procurarActionPerformed
        if((acessoUser.getText().equals("AT")||acessoUser.getText().equals("OF"))&&Integer.parseInt(mostrarNivel.getText())<5){
            JOptionPane.showMessageDialog(null,"VOCÊ NÃO PODE FAZER ALTERAÇÕES");
        }
        else if((acessoUser.getText().equals("ML")&&mloues.getText().equals("ES"))&&Integer.parseInt(mostrarNivel.getText())<5){
            JOptionPane.showMessageDialog(null,"VOCÊ NÃO PODE ADICIONAR UMA FOTO DE PEÇA NO BANCO DE DADOS DO ESTOQUE");
        }
        else if((acessoUser.getText().equals("ES")&&mloues.getText().equals("ML"))&&Integer.parseInt(mostrarNivel.getText())<5){
            JOptionPane.showMessageDialog(null,"VOCÊ NÃO PODE ADICIONAR UMA FOTO DE PEÇA NO BANCO DE DADOS DO MERCADO LIVRE");
        }
        else{
            JFileChooser arquivo = new JFileChooser();
            arquivo.setDialogTitle("SELECIONE UMA IMAGEM");
            arquivo.setFileSelectionMode(JFileChooser.FILES_ONLY);
            int opc = arquivo.showOpenDialog(this);
            if(opc==JFileChooser.APPROVE_OPTION){
                File file = new File("CAMINHO");
                file= arquivo.getSelectedFile();
                String filename = file.getAbsolutePath();
                caminhoDaImagem.setText(filename);
                ImageIcon imagen = new ImageIcon(arquivo.getSelectedFile().getPath());
                imagem.setIcon(new ImageIcon(imagen.getImage().getScaledInstance(imagem.getWidth(), imagem.getHeight(), Image.SCALE_DEFAULT)));
                if(mloues.getText().equals("ML")){
                    salvaM();
                }
                else{
                    salvaE();
                }
            }
        }
    }//GEN-LAST:event_procurarActionPerformed
    private void salvaM(){
        jLabel6.getIcon();
        String sql = "update tb_pecas_ml2 set IMAGEMLINK=? where CÓDIGO=?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1,caminhoDaImagem.getText());
            pst.setString(2,codigo.getText());
            pst.executeUpdate();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
