    private void abreimagemM(){
        String sqlnome = "select * from tb_pecas_ml2 where CÓDIGO=?";
        try {
            pst = conexao.prepareStatement(sqlnome);
            pst.setString(1,codigo.getText());
            rs = pst.executeQuery();
            if (rs.next()) {
                caminhoDaImagem.setText(rs.getString("IMAGEMLINK"));
                ImageIcon imagen = new ImageIcon(caminhoDaImagem.getText());
                imagem.setIcon(new ImageIcon(imagen.getImage().getScaledInstance(imagem.getWidth(), imagem.getHeight(), Image.SCALE_DEFAULT)));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }
