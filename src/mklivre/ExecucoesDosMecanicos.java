    private void remover(){
        int confirma = JOptionPane.showConfirmDialog(null, "TEM CERTEZA QUE DESEJA APAGAR ESSA EXECUÇÃO?","ATENÇÃO",JOptionPane.YES_NO_OPTION);
        if(confirma==JOptionPane.YES_OPTION){
            String sql = "delete from exec_of2 where os_aux=?";
            try {
                pst=conexao.prepareStatement(sql);
                pst.setString(1, campoOS.getText());
                int apagado = pst.executeUpdate();
                if(apagado>0){
                    JOptionPane.showMessageDialog(null,"EXECUÇÃO APAGADA COM SUCESSO");
                    this.dispose();
                }
                else{
                    JOptionPane.showMessageDialog(null,"A EXECUÇÃO NÃO EXISTE NO BANCO DE DADOS");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }
    private void historico(){
        String sqlnome = "select * from exec_of2 where os_aux=?";
        try {
            pst = conexao.prepareStatement(sqlnome);
            pst.setString(1,campoOS.getText());
            rs = pst.executeQuery();
            if (rs.next()) {
                historico.setText(rs.getString(17));
                campoObs.setText(rs.getString(6));
                aparelho.setText(rs.getString("APARELHO"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }
    private void consultar(){
        String sqlnome = "select * from exec_of2 where os_aux=?";
        try {
            pst = conexao.prepareStatement(sqlnome);
            pst.setString(1,campoOS.getText());
            rs = pst.executeQuery();
            if (rs.next()) {
                EditarExecucoes usuario = new EditarExecucoes();
                usuario.setVisible(true);
                usuario.mostrarNivel.setText(mostrarNivel.getText());
                usuario.acessoUser.setText(acessoUser.getText());
                usuario.cadastroOS.setText(campoOS.getText());
                usuario.servicoDaOs.setSelectedItem(rs.getString(2));
                usuario.cadastroObservacoes.setText(rs.getString(6));
                usuario.tipoDaOS.setSelectedItem(rs.getString(7));
                usuario.campoMecanicos.setSelectedItem(rs.getString(8));
                usuario.horaInicial.setValue(Integer.parseInt(rs.getString(10)));
                usuario.minutoInicial.setValue(Integer.parseInt(rs.getString(11)));
                usuario.horaFinal.setValue(Integer.parseInt(rs.getString(12)));
                usuario.minutoFinal.setValue(Integer.parseInt(rs.getString(13)));
                usuario.dataDia.setValue(Integer.parseInt(rs.getString(14)));
                usuario.dataMes.setValue(Integer.parseInt(rs.getString(15)));
                usuario.dataAno.setValue(Integer.parseInt(rs.getString(16)));
                usuario.historico.setText(rs.getString(17));
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(null,"EXECUÇÃO NÃO LOCALIZADA NO BANCO DE DADOS!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }
